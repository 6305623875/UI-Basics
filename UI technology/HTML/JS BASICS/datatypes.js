/*JS Data Types
The type of data which we can assign to a variable
1.Number->Any number
2.String->Any text data
3.Boolean->true,false
4.Undefined
5.Null*/

// String
let employeeName = 'john';
console.log(employeeName);
console.log(typeof employeeName);
console.log(`value : ${employeeName} Type : ${typeof employeeName}`);

//Number
let employyeAge = 40;
console.log(`value : ${employyeAge} Type : ${typeof employyeAge}`);

//Boolean
let isManager = true;
console.log(`value : ${isManager} Type : ${typeof isManager}`);

//Undefined
let name; 
console.log(name); 
console.log(`value : ${name} Type : ${typeof name}`);


//Null (dummy value)
let test = null;
console.log(`value : ${test} Type : ${typeof null}`);

//Arrays and Objects

// Re-assignment is allowed
let abc;
abc = 10;
abc = 'test';
abc = true;
console.log(`value : ${abc} type : ${typeof abc}`);


//string
let studentName = 'Anil';
console.log(studentName);
console.log(typeof studentName);
console.log(`value = ${studentName} type = ${typeof studentName}`);
//Number
let studentNumber = 102;
console.log(studentNumber);
console.log(typeof studentNumber);
console.log(`value = ${studentNumber} type = ${typeof studentNumber}`);
//Boolean
let isStudent = false;
console.log(isStudent);
console.log(typeof isStudent);
console.log(`value = ${isStudent} type = ${typeof isStudent}`);
//undefined
let student;
console.log(student);
console.log(typeof student);
//null
let anil = null;
console.log(`value = ${anil} type = ${typeof anil}`);

